package ex_04_Java_Operators;

public class Lab_06_Operators_LogicalOperators {
    static void main() {
        boolean a = true;
        System.out.println(a);
        System.out.println(!a);

        boolean b = true;
        System.out.println(b);
        System.out.println(!b);
        System.out.println(!!b);
        System.out.println(!!!b);

//        OR operator
        boolean c = true || false;
        System.out.println(c);
        System.out.println(!c);

//        AND Operator
        boolean d = true && false;
        System.out.println(d);


    }
}
