package ex_04_Java_Operators;

public class Lab_02_Operators_ArithmeticOperators {
    public static void main(String[] args) {
        // Arithmetic Operators
        //        + (Addition)
        //       - (Subtraction)
        //       * (Multiplication)
        //       / (Division)
        //       % (Modulus) | Modulus - Remainder

        int a = 20;
        int b = 3;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        float c = 3.0f;
        System.out.println(a/c);
        System.out.println("a+b");
        System.out.println(a+b);

    }
}
