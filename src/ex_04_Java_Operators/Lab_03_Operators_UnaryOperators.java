package ex_04_Java_Operators;

public class Lab_03_Operators_UnaryOperators {
    public static void main(String[] args) {
        // Unary Operator
        int a = +10;
        int a2 = 10; //by default, it is positive.
        int a1 = -110;
        int result = a+a1;
        System.out.println(result);

        int b = -1;
        b = b+1;
        System.out.println(b);
    }
}
