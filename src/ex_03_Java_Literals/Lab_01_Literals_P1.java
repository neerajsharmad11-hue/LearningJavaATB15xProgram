package ex_03_Java_Literals;

public class Lab_01_Literals_P1 {
    public static void main() {
        int age = 65;
        // data_type here is int
        // variable_name is age
        // = is operator
        //literal is 65
        System.out.println(age);

        float pi = 3.14f;
        // literal here is 3.14f
        System.out.println(pi);


        final int girl_age = 18;
        // 18 is literal here
        System.out.println(girl_age);

    }
}
