package ex_02_Java_Basics;

public class Lab10_Variables {
   public static void main(String[] args) {

//       data_type variable_name = variable_value
//       3 types of variable - local, static and instance.
//       If variable is within the method, it is called as local variable

        byte Age_Anil = 127;
       System.out.println("Age_Anil");

//       byte Age_Sunil = 128; This will not be accepted because max value what byte can store is 127 only
//       We can use bit
       short Age_Sunil = 128;
       System.out.println("Age_Sunil");




//       Here -
//       byte is data_type
//       Age_Anil is variable_name. Variable name is nothing but identifier
//       40 is variable_value. It is also called as Literal
//       "=" is called assignment operator. Its function is to put 40 into variable_name
//
    }


}